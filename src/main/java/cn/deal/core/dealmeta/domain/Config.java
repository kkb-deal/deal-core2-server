package cn.deal.core.dealmeta.domain;

/**
 * 配置
 *
 * @ClassName Config
 */
public class Config {

    /**
     * 方法
     */
    private String method;

    /**
     * url
     */
    private String url;

    public String getMethod() {
        return method;
    }

    public void setMethod(String method) {
        this.method = method;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
